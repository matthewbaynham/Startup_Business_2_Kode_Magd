        public static string GetString(string title, string sQuestion, string sOriginalValue, bool bReadOnly)
        {
            try
            {
                FrmLargeTextBox box = new FrmLargeTextBox { Text = title, Question = sQuestion, originalValue = sOriginalValue, ReadOnly = bReadOnly };

                if (box.ShowDialog() == DialogResult.OK)
                { return box.ResultText; }
                else
                { return string.Empty; }
            }
            catch (Exception ex)
            {
                MethodBase mbTemp = MethodBase.GetCurrentMethod();

                string sMessage = "";

                sMessage += "Add-in: " + mbTemp.ReflectedType.Name + "\n\r";
                sMessage += "Module Name: " + mbTemp.Module.Name + "\n\r";
